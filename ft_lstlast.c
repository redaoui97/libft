#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*ptr;

	ptr = lst;
	while (ptr)
	{
		if (ptr->next)
			ptr = ptr->next;
		else
			break ;
	}
	return (ptr);
}
